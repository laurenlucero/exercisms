// read the first bands color from an array
// return it's numeric value
// indexOf() method returns the first index at which a given element can be found in the array or -1 if element is not found

export const colorCode = (color) => COLORS.indexOf(color);

export const COLORS = [
  "black",
  "brown",
  "red",
  "orange",
  "yellow",
  "green",
  "blue",
  "violet",
  "grey",
  "white",
];
